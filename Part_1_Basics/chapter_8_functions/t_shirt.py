def make_shirt(size="large", message="I love Python."):
    """Display size of a shirt and a message to be printed on it."""
    print(f"\nThe shirt of size '{size}', will have the message '{message}'")


make_shirt("small", "Don't skip leg day!")
make_shirt(size="medium", message="Light weight baby.")
make_shirt()
make_shirt(size="medium")
