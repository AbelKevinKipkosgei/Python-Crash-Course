# Modifying, Adding, and Removing Elements
motorcycles = ["Honda", "Kawasaki", "Suzuki"]
print(motorcycles)
motorcycles[2] = "Harley Davidson"
print(motorcycles)
# Adding Elements
motorcycles.append("Ducati")
print(motorcycles)
motorcycles.insert(0, "KTM")
print(motorcycles)
# Removing Elements
del motorcycles[1]
print(motorcycles)
popped_motorcycle = motorcycles.pop()
print(motorcycles)
print(popped_motorcycle)
last_owned = motorcycles.pop()
print(f"The last motorcycle I owned was a {last_owned}.")
print(motorcycles)
first_owned = motorcycles.pop(0)
print(f"The first motorcycle I owned was a {first_owned}.")
print(motorcycles)
# Removing an Item by Value
motorcycles = ["honda", "yamaha", "suzuki", "ducati"]
print(motorcycles)
motorcycles.remove("ducati")
print(motorcycles)
too_basic = "honda"
motorcycles.remove(too_basic)
print(motorcycles)
print(f"A {too_basic.title()} is too basic for me.")
